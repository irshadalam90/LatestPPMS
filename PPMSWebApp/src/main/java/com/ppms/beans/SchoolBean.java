package com.ppms.beans;

public class SchoolBean {

	private Integer schoolId;
	private String schoolName;
	private String schoolCode;
	private String shoolLogo;
	private String shoolLocation;
	private Integer userId; 
	public Integer getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(Integer schoolId) {
		this.schoolId = schoolId;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolCode() {
		return schoolCode;
	}
	public void setSchoolCode(String schoolCode) {
		this.schoolCode = schoolCode;
	}
	public String getShoolLogo() {
		return shoolLogo;
	}
	public void setShoolLogo(String shoolLogo) {
		this.shoolLogo = shoolLogo;
	}
	public String getShoolLocation() {
		return shoolLocation;
	}
	public void setShoolLocation(String shoolLocation) {
		this.shoolLocation = shoolLocation;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	
	
}
